            <h1 class="rules_h">RULES AND REGULATIONS</h1>
            <p class="rules_bar">____________________________________</p>
            <br>
            <p><b>Group-3 School Rules and Regulations</b></p>
            
            <p>The administration and teachers of CGS believe that students must commit themselves to learn and accept responsibility for their work and behavior. Our goal is to instil qualities such as common courtesy, manners, respect for others, self-discipline, responsibility and accountability.
            
            We expect students to behave responsibly, recognizing the rights of others to be physically safe, emotionally secure and to pursue their own learning without interference. The CGS administration, teachers and parents need to work together in order to help students respect and abide by the established rules. Both teachers and parents have the potential to exercise a positive influence on the students when setting clearly defined limits and providing appropriate consequences for behavior.
            
            Parents, guardians and students are required to become familiar with all the Rules and Regulations at CGS, respect them at all times and adhere to the guidelines stipulated.
            The following school rules and regulations apply to student actions at all times.</p>
            
            <p><b>Arrival & Assembly</b></p>
            
            <p>Student must be in school 5 minutes before the start time. Students arriving after the stipulated arrival time for their classes will be locked out; i.e. they will not be allowed to attend school for that day. Apart from lock out being enforced, assembly and arrival times vary according to class. The ‘Class Notes’ in the school newsletter will clarify the respective ‘lock out’ time. No exceptions will be made for late arrivals, not even during exams. Students arriving after ‘lock out’ will be sent home, marked absent and will attain a ‘0’ for any missed tests or examinations.

            School assembly is held regularly except during Ramadan. A short verse from the Koran as well as school and national anthems are recited and sung. Information pertaining to school life and character building is also given out. The sports teacher conducts exercises which students are expected to follow. Students must be present at the assembly.
            
             Important announcements are made during assembly in addition to other activities such as awards.</p>
             
            <p><b>Student behavior</b></p>
            
            <p>In the school premises, students should:
            Be punctual for school and for individual classes. In senior classes, where students move between classes, every effort should be made to arrive punctually for lessons.
            
             
            Greet teachers and other staff members when they see them during the day.
            
             
            Stand up quickly and quietly when a teacher enters a classroom. Greet the teacher and sit quickly to commence lessons.
            
             
            Come to class prepared with all the materials needed and be ready to begin work on time.
            
             
            Raise their hands politely and wait to be called upon if they wish to speak during a class lesson. If the class is involved in an informal discussion, take turns when speaking. Students should give full attention to the teacher or student who is speaking and allow other students to concentrate on their work. They should behave in a manner that does not disrupt classroom learning or the operation of the school.
            
             
            Keep to the left of the stairs and corridors. Allow staff to go first.
            
             
            Refrain from pushing, running and shouting in the school.
            
             
            Share facilities with other students, e.g. table-tennis, computers etc.
            
             
            Speak in English at all times except during Bangla classes.
            
             
            Maintain silence in the library, leaving the tables clear of books and chairs in their original place when leaving.
            
             
            Not remain on school premises after school, unless for special classes, ECAs or if asked by a staff member.
            
             
            Refrain from inappropriate physical intimacy with other students. Students found in compromising positions will be immediately expelled.</p>    
            
            <p><b>Picking up/dropping off students</b></p>
            
            <p>Drivers picking up and dropping off students must queue in a single file at the gates.
            
             
            Drivers should not double park or use the horn (unless to avoid an accident).
            
             
            ‘U-turns’ should be made in the designated U-turn Zone.
            
             
            Students whose drivers do not adhere to traffic rules will be issued a warning letter. Should this happen again a final warning will be issued and on the third occasion the student will be suspended or expelled.
            
             
            Anybody, including parents coming to pick up a student, in Playgroup to Class 7, must present the student’s I.D. card. Under no circumstances shall the student be handed over to anyone without the student’s I.D. card. Parents are requested to remain in their cars during drop-off and pick-up.
            
             
            In circumstances when parents or guardians enter the school gate to drop off / pick up their child/children, they must wait at the designated area and not be freely roaming around the school premises.</p>
            
             
            <p><b>Attendance and Absence Procedures</b></p>
            
            <p>Students are required to attend school regularly.
            
             
            Students should never leave the school premises for any reason during the school hours unless accompanied by a parent, guardian or teacher, after being properly signed out at the school office.
            
             
            Whenever a student is absent from school for any reason, the following procedure should be followed. Firstly, parents should telephone the school office the first morning of the student’s illness to notify the school of the absence. This applies also to cases of grief, family illness, sudden emergencies, etc. In addition, upon return to school the student must bring a written explanation for absence on the special school form provided at the office. Requests for holiday or absence must be made three (3) clear days in advance on the form available at the school office. Approval is not automatic. If the authority feels that the academics of the students might suffer, the leave might not be approved.
            
             
            Failure to follow absence procedures (for example ‘unauthorized absence’) will result in suspension.
            
             
            In order to encourage full school attendance, a 100% ‘Attendance Award’ is given to all students who achieve this. Attendance at all school events is mandatory in order to qualify for this award.
            
             
            Parents should try to instil in their children the need to be regular at school.
            
             
            After regular school hours students who have ECAs or extra classes must remain in school for lunch. Lunch may be purchased at the school canteens or brought at the start of the school day. Parents are not allowed to send food items to school at any time during the school day, except with the student in the morning.</p>
            
             
            <p><b>Homework, Class work and Tests</b></p>
            
            <p>Homework is set regularly and students are encouraged to work without the help of tutors.
            
             
            Parents are requested to sign their child’s diary daily in order to be well-informed of homework, assignments and notices given by teachers. Failure to do so may result in miscommunication between parents and teachers.
            
             
            Students are expected to complete homework and class work assignments neatly, carefully, and on time.
            
             
            Students should work to their full potential.
            
             
            If a student experiences difficulty in completing an assignment with ‘good cause’, the student’s parent should explain the situation to the teacher with a short note in his or her diary.
            
             
            There are no make-up tests in CGS. All absences during tests are marked with a ‘0’. However, each teacher is advised to take extra tests each assessment period and base the final marks on a percentage of these, more commonly known as the ‘best-of’ method.
            
             
            When students have been absent for more than 20% of the term time they are not given an assessment report. In this case, the school will not be held responsible for the child’s progress and there is a good chance that he/she will lose ground in class and have to repeat the year.
            
             
            Students who are ill will not be allowed to come to school just to sit for tests or an examination.
            
             
            Test and examination grades will be kept confidential and only disclosed to the concerned student’s parents.
            
             
            Generally there are very limited tests or examinations for students in the junior classes.</p>
            
             
            <p><b>Private Tuition</b></p>
            
             <p>Private tuition is not encouraged unless written permission is given by the Head of School.
            
             
            Students taking private tuition from a CGS teacher without written permission will be expelled immediately.
            
             
            The school regularly arranges remedial classes for students requiring extra attention.
            
             
            Parents, guardians or students are advised not to present any gifts to teachers. Any gift-giving could result in the suspension or expulsion of the student.
            
            The CGS diary is the easiest, most accessible and the preferred method of communication between the school and parents. Messages for individual teachers and comments or queries about homework should be noted in the diary.
            
             
            School diaries should be checked and signed by parents daily.
            
             
            In addition to homework assignments, it often contains important notices and information.
            
             
            Minor issues can be dealt by the class teacher through messages exchanged via the student’s diary.
            
             
            The diary check record at the beginning of the diary, the health and hygiene sheet and fortnightly reports at the end of the diary must be signed by the parent / guardian on a regular basis in order to maintain consistency throughout the school year.</p>
            
             
            <p><b>Respect for School and Personal Property</b></p>
            
            <p>Students are required to be respectful of others belongings, using them only with permission. A student who steals or damages any property not belonging to him/her may be required to make appropriate restitution. Though the school manages a lost and found at the school office, students are strongly encouraged to take good care of their personal belongings. Parents should mark their child’s special belongings with names and I.D. numbers. Lockers have also been provided on some premises and students are encouraged to take one for this purpose.
            
             
            Students are required to use equipment and materials in school for their intended purposes only. They will be held accountable for improper care of school books, educational materials, school furniture, sports and laboratory equipment and their classrooms.
            
             
            ‘AS’ and ‘A’ Level science students will be required to put a refundable deposit of Taka 2,000/- for use of the laboratories. Any damage or breakage will be deducted from this deposit.
            
             
            Parents of students who damage, misplace, deface or destroy school property will be billed for damages.
            
             
            In addition, the student will be accorded appropriate punishment by the disciplining authority.
            
             
            Students should make every effort to respect school property and keep it neat and clean at all times.
            
             
            Students should not bring valuable items to school including expensive pens, jewellery, watches, electronic equipment (including mobile phones, CD’s and CD players) and unreasonable amounts of cash. Students are encouraged not to bring sums of money to school unless absolutely necessary, nor should they lend to or borrow from other students. CGS will not bear responsibility for recovering any money that has been loaned or valuables that may be alleged to be stolen. Disallowed equipment will be confiscated by the school authorities if found by teachers during random checks and only be returned at the end of the school year to parents or guardians.
            
             
            The school provides internet access to students and expects them to use this facility with respect and adherence to guidelines provided by their IT teacher. Should a student be found to be accessing pornography or other undesirable websites he / she will be issued a warning and their privilege of internet facilities shall be withdrawn. In some cases this could also lead to expulsion.</p>
                        
            <p><b>Courtesy, Integrity and Respect Others</b></p>
            
             <p>Courtesy, integrity, manners and respect for others are virtues highly valued and encouraged at CGS. Treat others with kindness, respect and courtesy.
            
             
            Students should attempt to settle disagreements with others through discussion, asking for help if required.  Interact and speak to others in kindness only.
            
             
            Students are to refrain from aggressive or threatening behavior towards fellow students, teachers or other school staff. Fighting or rough play are strictly prohibited in school. If a student experiences a problem with another student, he or she should notify the nearest teacher or staff member immediately. Students should be instructed in ways to avoid getting involved in conflicts.
            
             
            Bullying in any form is strictly prohibited by the school Students who are found to be exerting any form of bullying may be suspended or expelled. Any form of bullying experienced by any student needs to be reported to the school authority by the respective parents.
            
             
            If parents or students wish to report instances of bullying confidentially, this confidentiality will be respected. It should be noted however, that it is not possible to punish an alleged bully on the basis of any report without giving the offender an opportunity to answer to the specific allegations.</p>
            
            <p><b>Fees and Payments</b></p>
            
            <p>All fees and charges must be paid by the stipulated due date. Failure to do so may result in the cancellation of enrolment.
            
             
            End-of-term report cards will not be issued unless all school bills have been cleared.
            
             
            For those students who wish to remain at CGS the following year, parents must ensure that the miscellaneous charges are paid within the stipulated period of time given by the accounts office.
            
             
            The school reserves the right to change any fees or charges without prior notice.
            
             
            Transfer or Graduation Certificates will not be issued unless all dues have been cleared.
            
             
            Parents are requested to carefully read, sign and conform to the payment schedules and Rules and Regulations laid out by the accounts office.</p>
            
             
            <p><b>Prohibited in School or on School outings, tours or trips</b></p>  
            
            <p>Students must refrain from the possession or use of any illegal drugs, alcohol, profane magazines or tobacco products. Failure to respect this regulation may have legal consequences resulting in immediate suspension or expulsion. Gambling, immorality, disrespectful or profane language will not be tolerated in the school or on school grounds and can lead to suspension or expulsion. Mobiles, camcorders and other electronic gadgets (other than calculators) are prohibited in school (unless asked by a teacher to bring in for a particular project) and will be confiscated and returned only at the end of the school year after a clearance certificate has been obtained.</p>
             
            <p><b>Dress Code</b></p>
            
            <p>Students are expected to wear the correct and complete school uniform to school each day as well as the appropriate sports/ games uniform during games, ECA’s and gym classes.
            
             
            Neatness and cleanliness are encouraged. Uniforms should be clean and pressed, shoes should be polished.
            
             
            Students arriving in incorrect or incomplete uniform will be punished and parents will be required to send the appropriate uniform to school.
            
             
            Students are not allowed to wear make-up to school or any kind of trinkets, baubles, or coloured ribbons, other than black, blue or white. No hair dye is to be used by students.
            
             
            Sports and gym uniforms must be worn as stipulated. Students feeling cold may wear school sweaters or blazers.
            
             
            CGS has specific winter and summer dress codes. During winter session ties must be worn throughout the day, with long-sleeved shirts and cuffs neatly buttoned up.
            
             
            During gym/games days, students must come to school in gym/games kits.</p>
            
            <p><b>Cheating and Plagiarism</b></p>
            
            <p>Any form of cheating, whether in class work, class tests or examinations will be dealt with very strictly and could lead to suspension or expulsion. Instances of plagiarism (copying the work of others) will also be considered for suspension or expulsion.</p>

            <p><b>Parents entering school premises</b></p>

            <p>Parents are not allowed to come inside the school premises unless to meet a teacher or member of the administrative staff. When stopped at the school gate by the security, parents must comply with the guidelines given by the guards instead of behaving in a hostile manner and being abusive. The student may be suspended in case of misbehaviour by the respective parent or representative. In some cases this may also lead to immediate expulsion if supported by the footage of the CC Camera coverage and findings of the Investigation Committee. In order to meet a particular member of the faculty or staff, parents or guardians must make prior appointments with the school office. In case of emergencies parents or guardians may contact the school office and every effort will be made to resolve the matter. Please note that we discourage parents or guardians to call on the personal contact numbers of any CGS members of staff. Parents cannot enter the school or classrooms at any time unless specifically invited. When visiting or when arriving for appointments they should contact the office first. Parents cannot send money to the class teachers to purchase tiffin or send tiffin in the middle of the school day. They should send it with the student. Classes cannot be interrupted to give students books, money or other items. These will be given at the next break in class. Any parent wanting homework for an absent student must contact the appropriate teacher after school through the school office. Parents are advised to label all belongings and clothing with the student’s name and I.D. number to prevent loss and to help teachers to identify them during class. Parents are requested not to directly telephone teachers or other staff members after school hours. However, for any urgent matter, text messages will be accomodated/accepted. During school hours the school office should be contacted and the matter will be taken forward. Parents are not allowed to send cakes and other edibles for birthdays. Parents visiting the school by appointment must register at the front gate in the visitor’s book. During class visits, parents are requested not to bring presents or edibles for teachers or students.</p>
            
            <p><b>Security, Traffic and Parking</b></p> 

            <p>All premises have installed CC Cameras at various points to keep a close vigilance on the activities being carried out within and directly outside the school premises.
            
             
            The school employs a large security team who patrol the school grounds 24 hours a day. When necessary (especially during after-school functions) students are thoroughly checked for prohibited items. Possession of these may lead to immediate suspension or expulsion.
            
             
            The security team is also responsible for the smooth flow of traffic outside the school premises. Any cars violating school traffic-flow rules will lead to sanctions against the student whose car has violated the rules. In case of disputes the judgement of the Head of Security will be final.
            
             
            Traffic flow rules will also include designation of parking areas, no U-turn zones and one-way flow of traffic.
            
             
            Repeat offenders of traffic flow rules could lead to the student being suspended or expelled.</p>
            
             
            <p><b>Transfer Certificates</b></p>
            
            <p>Parents are required to give, in writing, two months’ notice of intention to withdraw their child. Unless the required notice is given a transfer certificate will not be issued and the parents will be liable to pay fees until the end of the school year. Parents withdrawing their child at the end of the academic year are liable for July and August fees, as these months are part of the preceding school year. Transfer Certificate will only be issued after students have obtained a clearance certificate from the school accountant, librarian, class teacher and office.</p>

             <p><b>Suspension and Expulsion</b></p>

            <p>Parents may be required, during or at the end of a term, to withdraw the student, without refund of fees temporarily or permanently from the school if, after consultation with a parent, the Head is of the opinion that the conduct or progress of the student has been unsatisfactory or if the student, in the judgment of the Head, is unwilling or unable to profit from the educational opportunities offered, or if a parent has treated the school or members of its staff unreasonably, and in any such case removal is considered to be warranted.
            
             
            A student may be expelled at any time if the Head is reasonably satisfied that the student’s conduct (whether on or off school premises or in or out of term time) has been prejudicial to good order or school discipline or to the reputation of the school. The Head will act fairly and in accordance with the procedures of natural justice and will not expel a student other than in grave circumstances. There will be no refund of fees following expulsion (and all unpaid fees must be paid).
            
             
            The decision to exclude, suspend, remove or expel a student and the manner and form of any announcement shall be at the discretion of the Head and the Disciplinary Committee. Under no circumstances shall the school be required to divulge any information to parents / guardians pertaining to investigations carried out during suspension / removal / expulsion.
            
             
            A student who has been withdrawn, excluded, suspended removed or expelled from the school has no right to enter the school premises without the written permission of the Head.
            
            Suspension and Expulsions from CGS are serious actions that have been defined in the CGS constitution and reproduced in the Appendix that follows.</p>
            
            <p><b>Enforcing School Rules and Regulations</b></p>
            
            <p>CGS students are supplied with a copy of the School Rules and Regulations and all stakeholders, including parents, guardians and students are expected to be familiar with this document. The CGS Management makes every effort to apply rules in fairness to all. Students violating any of the School Rules and Regulations are subject to disciplinary action. Student prefects, teachers, Administrators, and Heads of School are all responsible for enforcing the rules and regulations stipulated above and for allocating appropriate punishment.
            In case of a serious breach of school rules or regulation the school disciplinary committee will be called upon to take appropriate disciplinary measures, such as suspension, removal or expulsion.
            If a student is suspended twice he or she is expected to be withdrawn from the school by parents or guardians. If the student is not withdrawn, he or she will be issued an expulsion notice by the disciplinary committee.
            The school reserves the right to suspend or expel any student for violating any of the School Rules and Regulations.
            All parents are required to sign an acceptance form stating that they understand and accept the disciplinary procedures as laid out in the School Rules and Regulations.</p>
